package com.green.java.ch02;

public class VariableNumber {
    public static void main(String[] args) {
        //변수 숫자형
        int n1; //(정수형)변수 선언 //4byte
        n1 = 10; //=은 대입연산자, 오른쪽 값 복사하여 왼쪽에 주입

        System.out.println(n1);

        n1 = 20;
        System.out.println(n1);
    }
}
