package com.green.java.ch03;

public class OperatorStudy1 {
    public static void main(String[] args) {
        int num = 5;
        int result = 4 + num * 3;
        System.out.println(result);

        result = 11 / num;
        System.out.println(result);

        result = result - 1;
        System.out.println(result);

        result = 13 % num;
        System.out.println(result);
        //모드로 짝수인지 홀수 인지 알 수 있다.
    }
}
